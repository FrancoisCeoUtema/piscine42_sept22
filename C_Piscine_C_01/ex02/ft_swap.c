#include <unistd.h>

void	ft_swap(int *a, int *b)
{
	int	temp;

	temp = *a;
	*a = *b;
	*b = temp;
}

int	main(void)
{
	int	a1;
	int	b1;

	a1 = 42;
	b1 = 52;
	ft_swap(&a1, &b1);
	return (0);
}
