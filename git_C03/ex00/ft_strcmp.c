#include <stdio.h>

int	ft_strcmp(char *s1, char *s2)
{
	while (*s1 != *s2 || *s1 != '\0' || *s2 != '\0')
	{
		if (*s1 < *s2)
		{
			return (-1);
		}
		if (*s1 > *s2)
		{
			return (1);
		}
		s1++;
		s2++;
	}
	return (0);
}

int	main(void)
{
	int	i;

	i = ft_strcmp("ABB", "ABB");
	printf("%d", i);
	return (0);
}
