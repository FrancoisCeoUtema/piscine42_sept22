#include <stdio.h>

int	ft_sqrt(int nb)
{
	int	i;

	i = 0;
	while (i*i != nb)
	{
		if ( i*i > nb)
		{
			return (0);
		}
		i++;
	}
	return (i);
}

int	main(void)
{
	printf("%d\n", ft_sqrt(25));
	return (0);
}
